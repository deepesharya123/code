/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    
    public ListNode reverse(ListNode head){
        ListNode prev = null, cur = head;
        while( cur != null){
            ListNode nex = cur.next;
            cur.next = prev;
            prev = cur;
            cur = nex;
        }
        return prev;
    }
    
    public ListNode reverseBetween(ListNode head, int left, int right) {
        ListNode dummyHead = new ListNode(-1111,head);
        ListNode past = dummyHead, trav = head, ret = head, future = null ;
        // past = previous of left, future = next of right
        int k = 1;
        ListNode rev = null;
        if(left == right)
            return head;
        while( k <= right + 1){
            if(trav == null )
                break;
            if( k < left - 1 )
                k++;
            else if( k == left - 1){
                past = trav;
                k++;
            }else if(k == left){
                rev = trav;
                k++;
            }else if( k > left && k < right )
                k++;
            else if( k == right ){
                future = trav.next;
                trav.next = null;
                k++;
                break;
            }
            trav = trav.next;
        }
        ListNode reversed = reverse(rev);
        ListNode temp = reversed;
        while(temp.next != null)
            temp = temp.next;
        // if(past != null)
        past.next = reversed;
        // else
        //     head
        temp.next = future;
        return dummyHead.next;
    }
}

